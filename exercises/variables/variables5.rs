// 在 rust 中 可以声明同名但不同类型的变量
fn main() {
    let number = "T-H-R-E-E"; // don't change this line
    println!("Spell a Number : {}", number);
    let number = 3; // don't rename this variable
    print!("Number plus two is : {}", number + 2);
}
